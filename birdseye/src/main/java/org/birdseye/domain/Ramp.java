package org.birdseye.domain;


public class Ramp {

    private String exit;
    private String name;
    private String lat;
    private String lng;

    public String getExit() {
        return exit;
    }

    public void setExit(final String exit) {
        this.exit = exit;
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public String getLat() {
        return lat;
    }

    public void setLat(final String lat) {
        this.lat = lat;
    }

    public String getLng() {
        return lng;
    }

    public void setLng(final String lng) {
        this.lng = lng;
    }
}
